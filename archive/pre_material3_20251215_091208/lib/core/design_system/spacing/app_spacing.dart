/// Design System Spacing
/// Standard spacing values for margins, padding, and gaps
class AppSpacing {
  // ========== Base Spacing ==========
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 12.0;
  static const double lg = 16.0;
  static const double xl = 20.0;
  static const double xxl = 24.0;
  static const double xxxl = 28.0;
  static const double huge = 32.0;

  // ========== Specific Use Cases ==========
  /// Standard padding for screen edges
  static const double screenPadding = 20.0;

  /// Padding inside cards
  static const double cardPadding = 16.0;

  /// Small card padding
  static const double cardPaddingSmall = 12.0;

  /// Space between sections
  static const double sectionSpacing = 24.0;

  /// Space between elements in a section
  static const double elementSpacing = 12.0;

  /// Radius for rounded corners on sections
  static const double sectionBorderRadius = 52.0;

  /// Radius for cards
  static const double cardBorderRadius = 16.0;

  /// Small radius for buttons and inputs
  static const double buttonBorderRadius = 12.0;
}
